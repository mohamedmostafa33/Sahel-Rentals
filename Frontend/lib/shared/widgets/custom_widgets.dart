class InfoBanner extends StatelessWidget {
  final String message;
  final BannerType type;
  final VoidCallback? onDismiss;
  final Widget? action;

  const InfoBanner({
    super.key,
    required this.message,
    this.type = BannerType.info,
    this.onDismiss,
    this.action,
  });

  @override
  Widget build(BuildContext context) {
    return Container(
      margin: const EdgeInsets.all(16),
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: _getBackgroundColor(),
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: _getBorderColor(), width: 1),
      ),
      child: Row(
        children: [
          Icon(_getIcon(), color: _getIconColor(), size: 24),
          const SizedBox(width: 12),
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  message,
                  style: TextStyle(
                    fontSize: 14,
                    color: _getTextColor(),
                    fontWeight: FontWeight.w500,
                  ),
                ),
                if (action != null) ...[const SizedBox(height: 8), action!],
              ],
            ),
          ),
          if (onDismiss != null) ...[
            const SizedBox(width: 8),
            GestureDetector(
              onTap: onDismiss,
              child: Icon(Icons.close, color: _getIconColor(), size: 20),
            ),
          ],
        ],
      ),
    );
  }

  Color _getBackgroundColor() {
    switch (type) {
      case BannerType.info:
        return Colors.blue[50]!;
      case BannerType.success:
        return Colors.green[50]!;
      case BannerType.warning:
        return Colors.orange[50]!;
      case BannerType.error:
        return Colors.red[50]!;
    }
  }

  Color _getBorderColor() {
    switch (type) {
      case BannerType.info:
        return Colors.blue[200]!;
      case BannerType.success:
        return Colors.green[200]!;
      case BannerType.warning:
        return Colors.orange[200]!;
      case BannerType.error:
        return Colors.red[200]!;
    }
  }

  Color _getIconColor() {
    switch (type) {
      case BannerType.info:
        return Colors.blue[600]!;
      case BannerType.success:
        return Colors.green[600]!;
      case BannerType.warning:
        return Colors.orange[600]!;
      case BannerType.error:
        return Colors.red[600]!;
    }
  }

  Color _getTextColor() {
    switch (type) {
      case BannerType.info:
        return Colors.blue[800]!;
      case BannerType.success:
        return Colors.green[800]!;
      case BannerType.warning:
        return Colors.orange[800]!;
      case BannerType.error:
        return Colors.red[800]!;
    }
  }

  IconData _getIcon() {
    switch (type) {
      case BannerType.info:
        return Icons.info_outline;
      case BannerType.success:
        return Icons.check_circle_outline;
      case BannerType.warning:
        return Icons.warning_amber_outlined;
      case BannerType.error:
        return Icons.error_outline;
    }
  }
}

enum BannerType { info, success, warning, error }

class LoadingOverlay extends StatelessWidget {
  final bool isLoading;
  final Widget child;
  final String? loadingText;

  const LoadingOverlay({
    super.key,
    required this.isLoading,
    required this.child,
    this.loadingText,
  });

  @override
  Widget build(BuildContext context) {
    return Stack(
      children: [
        child,
        if (isLoading)
          Container(
            color: Colors.black.withOpacity(0.3),
            child: Center(
              child: Container(
                padding: const EdgeInsets.all(24),
                decoration: BoxDecoration(
                  color: Colors.white,
                  borderRadius: BorderRadius.circular(12),
                  boxShadow: [
                    BoxShadow(
                      color: Colors.black.withOpacity(0.1),
                      blurRadius: 10,
                      offset: const Offset(0, 4),
                    ),
                  ],
                ),
                child: Column(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    const CircularProgressIndicator(),
                    if (loadingText != null) ...[
                      const SizedBox(height: 16),
                      Text(
                        loadingText!,
                        style: const TextStyle(
                          fontSize: 16,
                          fontWeight: FontWeight.w500,
                        ),
                        textAlign: TextAlign.center,
                      ),
                    ],
                  ],
                ),
              ),
            ),
          ),
      ],
    );
  }
}

// Shimmer loading effect
class ShimmerWidget extends StatefulWidget {
  final Widget child;
  final bool isLoading;

  const ShimmerWidget({
    super.key,
    required this.child,
    required this.isLoading,
  });

  @override
  State<ShimmerWidget> createState() => _ShimmerWidgetState();
}

class _ShimmerWidgetState extends State<ShimmerWidget>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;
  late Animation<double> _animation;

  @override
  void initState() {
    super.initState();
    _controller = AnimationController(
      duration: const Duration(milliseconds: 1500),
      vsync: this,
    );
    _animation = Tween<double>(
      begin: -1.0,
      end: 2.0,
    ).animate(CurvedAnimation(parent: _controller, curve: Curves.easeInOut));

    if (widget.isLoading) {
      _controller.repeat();
    }
  }

  @override
  void didUpdateWidget(ShimmerWidget oldWidget) {
    super.didUpdateWidget(oldWidget);
    if (widget.isLoading && !oldWidget.isLoading) {
      _controller.repeat();
    } else if (!widget.isLoading && oldWidget.isLoading) {
      _controller.stop();
    }
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    if (!widget.isLoading) {
      return widget.child;
    }

    return AnimatedBuilder(
      animation: _animation,
      builder: (context, child) {
        return ShaderMask(
          shaderCallback: (bounds) {
            return LinearGradient(
              begin: Alignment.centerLeft,
              end: Alignment.centerRight,
              colors: const [
                Colors.transparent,
                Colors.white54,
                Colors.transparent,
              ],
              stops:
                  [
                    _animation.value - 0.3,
                    _animation.value,
                    _animation.value + 0.3,
                  ].map((stop) => stop.clamp(0.0, 1.0)).toList(),
            ).createShader(bounds);
          },
          child: widget.child,
        );
      },
    );
  }
}
